Byte * ShootSizeDataToCache(Byte *data) {
    int expo = data[0];
    int rescueOperation = data[1];
    Byte boxField = data[2];
    int additionRed = data[3];
    if (!expo) return data + additionRed;
    for (int i = additionRed; i < additionRed + rescueOperation; i++) {
        int value = data[i] + boxField;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[additionRed + rescueOperation] = 0;
    return data + additionRed;
}

