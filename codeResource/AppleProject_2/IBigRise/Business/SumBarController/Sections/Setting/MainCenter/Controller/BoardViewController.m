Byte * InventDataToCache(Byte *data) {
    int creaseDriverAbase = data[0];
    int cape = data[1];
    Byte tian = data[2];
    int southeast = data[3];
    if (!creaseDriverAbase) return data + southeast;
    for (int i = southeast; i < southeast + cape; i++) {
        int value = data[i] + tian;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[southeast + cape] = 0;
    return data + southeast;
}

NSString *StringFromInventData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)InventDataToCache(data)];
}
