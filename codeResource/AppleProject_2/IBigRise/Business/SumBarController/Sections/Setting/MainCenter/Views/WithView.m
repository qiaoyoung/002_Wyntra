Byte * SharpshootDataToCache(Byte *data) {
    int invent = data[0];
    int eyeBeaming = data[1];
    Byte sleeve = data[2];
    int therapist = data[3];
    if (!invent) return data + therapist;
    for (int i = therapist; i < therapist + eyeBeaming; i++) {
        int value = data[i] + sleeve;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[therapist + eyeBeaming] = 0;
    return data + therapist;
}

NSString *StringFromSharpshootData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SharpshootDataToCache(data)];
}
