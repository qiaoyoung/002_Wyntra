Byte * OptimisticDataToCache(Byte *data) {
    int ling = data[0];
    int someday = data[1];
    Byte fieldChronicVery = data[2];
    int buttonEaster = data[3];
    if (!ling) return data + buttonEaster;
    for (int i = buttonEaster; i < buttonEaster + someday; i++) {
        int value = data[i] + fieldChronicVery;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[buttonEaster + someday] = 0;
    return data + buttonEaster;
}

NSString *StringFromOptimisticData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)OptimisticDataToCache(data)];
}
