Byte * ScaleRecordingDataToCache(Byte *data) {
    int sizeText = data[0];
    int lengthView = data[1];
    Byte cherryCurrent = data[2];
    int timeTapHeight = data[3];
    if (!sizeText) return data + timeTapHeight;
    for (int i = timeTapHeight; i < timeTapHeight + lengthView; i++) {
        int value = data[i] - cherryCurrent;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[timeTapHeight + lengthView] = 0;
    return data + timeTapHeight;
}

NSString *StringFromScaleRecordingData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ScaleRecordingDataToCache(data)];
}
