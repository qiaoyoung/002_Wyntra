Byte * FibDataToCache(Byte *data) {
    int cask = data[0];
    int change = data[1];
    Byte elsewhereSharp = data[2];
    int chiropractorGory = data[3];
    if (!cask) return data + chiropractorGory;
    for (int i = chiropractorGory; i < chiropractorGory + change; i++) {
        int value = data[i] + elsewhereSharp;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[chiropractorGory + change] = 0;
    return data + chiropractorGory;
}

NSString *StringFromFibData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)FibDataToCache(data)];
}
