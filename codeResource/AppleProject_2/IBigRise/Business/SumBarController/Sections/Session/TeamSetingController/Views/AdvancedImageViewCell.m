Byte * EntertainSharpDataToCache(Byte *data) {
    int rage = data[0];
    int sitting = data[1];
    int withMedia = data[2];
    if (!rage) return data + withMedia;
    for (int i = 0; i < sitting / 2; i++) {
        int begin = withMedia + i;
        int end = withMedia + sitting - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[withMedia + sitting] = 0;
    return data + withMedia;
}

NSString *StringFromEntertainSharpData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)EntertainSharpDataToCache(data)];
}  
