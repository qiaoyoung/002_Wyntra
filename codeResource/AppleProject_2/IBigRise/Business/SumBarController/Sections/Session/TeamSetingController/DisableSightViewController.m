Byte * FainDataToCache(Byte *data) {
    int mediaOration = data[0];
    int kingdomTap = data[1];
    Byte dailyVariation = data[2];
    int entertain = data[3];
    if (!mediaOration) return data + entertain;
    for (int i = entertain; i < entertain + kingdomTap; i++) {
        int value = data[i] - dailyVariation;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[entertain + kingdomTap] = 0;
    return data + entertain;
}

NSString *StringFromFainData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)FainDataToCache(data)];
}
