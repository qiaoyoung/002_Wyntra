Byte * PlayoffDataToCache(Byte *data) {
    int texture = data[0];
    int teaCozy = data[1];
    Byte worry = data[2];
    int pavement = data[3];
    if (!texture) return data + pavement;
    for (int i = pavement; i < pavement + teaCozy; i++) {
        int value = data[i] + worry;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[pavement + teaCozy] = 0;
    return data + pavement;
}

NSString *StringFromPlayoffData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PlayoffDataToCache(data)];
