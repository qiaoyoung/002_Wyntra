Byte * MediaDataToCache(Byte *data) {
    int complete = data[0];
    int colorBox = data[1];
    int use = data[2];
    if (!complete) return data + use;
    for (int i = 0; i < colorBox / 2; i++) {
        int begin = use + i;
        int end = use + colorBox - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[use + colorBox] = 0;
    return data + use;
}

NSString *StringFromMediaData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)MediaDataToCache(data)];
}  
