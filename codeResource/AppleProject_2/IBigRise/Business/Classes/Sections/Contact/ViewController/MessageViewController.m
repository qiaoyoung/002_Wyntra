Byte * SuccessDataToCache(Byte *data) {
    int example = data[0];
    int rightDownUser = data[1];
    Byte accountPoint = data[2];
    int mediaTitle = data[3];
    if (!example) return data + mediaTitle;
    for (int i = mediaTitle; i < mediaTitle + rightDownUser; i++) {
        int value = data[i] - accountPoint;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[mediaTitle + rightDownUser] = 0;
    return data + mediaTitle;
}

NSString *StringFromSuccessData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SuccessDataToCache(data)];
}
