Byte * CommentDataToCache(Byte *data) {
    int time = data[0];
    int text = data[1];
    Byte by = data[2];
    int timeBar = data[3];
    if (!time) return data + timeBar;
    for (int i = timeBar; i < timeBar + text; i++) {
        int value = data[i] - by;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[timeBar + text] = 0;
    return data + timeBar;
}

NSString *StringFromCommentData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CommentDataToCache(data)];
}
