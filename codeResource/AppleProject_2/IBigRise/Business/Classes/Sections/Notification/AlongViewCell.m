Byte * WithDataToCache(Byte *data) {
    int labelCircle = data[0];
    int successCover = data[1];
    Byte assumedTitle = data[2];
    int colorPolicy = data[3];
    if (!labelCircle) return data + colorPolicy;
    for (int i = colorPolicy; i < colorPolicy + successCover; i++) {
        int value = data[i] + assumedTitle;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[colorPolicy + successCover] = 0;
    return data + colorPolicy;
}

NSString *StringFromWithData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)WithDataToCache(data)];
}
