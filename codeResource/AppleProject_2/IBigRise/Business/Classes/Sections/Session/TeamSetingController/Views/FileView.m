Byte * DominantDataToCache(Byte *data) {
    int lysisEaster = data[0];
    int topWarningRecording = data[1];
    int allOrNone = data[2];
    if (!lysisEaster) return data + allOrNone;
    for (int i = 0; i < topWarningRecording / 2; i++) {
        int begin = allOrNone + i;
        int end = allOrNone + topWarningRecording - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[allOrNone + topWarningRecording] = 0;
    return data + allOrNone;
}

NSString *StringFromDominantData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)DominantDataToCache(data)];
}  
