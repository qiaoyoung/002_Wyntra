Byte * VaryDataToCache(Byte *data) {
    int job = data[0];
    int accountAdventure = data[1];
    Byte somedayTitle = data[2];
    int forwarding = data[3];
    if (!job) return data + forwarding;
    for (int i = forwarding; i < forwarding + accountAdventure; i++) {
        int value = data[i] - somedayTitle;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[forwarding + accountAdventure] = 0;
    return data + forwarding;
}

