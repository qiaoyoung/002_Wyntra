Byte * MentalImageDataToCache(Byte *data) {
    int ideology = data[0];
    int building = data[1];
    Byte mareDeveloping = data[2];
    int specially = data[3];
    if (!ideology) return data + specially;
    for (int i = specially; i < specially + building; i++) {
        int value = data[i] - mareDeveloping;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[specially + building] = 0;
    return data + specially;
}

NSString *StringFromMentalImageData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)MentalImageDataToCache(data)];
}
